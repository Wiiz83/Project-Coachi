package miage.al3c.g4.coachi.Utilitaire;

import android.graphics.Bitmap;

public class Badge {
    private String nomBadge;
    private int idBadge;
    private Bitmap asp;

    public String getNomBadge() {
        return nomBadge;
    }

    public void setNomBadge(String nomBadge) {
        this.nomBadge = nomBadge;
    }

    public int getIdBadge() {
        return idBadge;
    }

    public void setIdBadge(int idBadge) {
        this.idBadge = idBadge;
    }

    public Bitmap getAsp() {
        return asp;
    }

    public void setAsp(Bitmap asp) {
        this.asp = asp;
    }
}
